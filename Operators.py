'''
Operatores perform operations on variable and values
variables are the data operators are instructions what to do with data
'''
print(1 + 1)

print(3 * 5)

print(5 ** 2) # exponent

print(20 / 5)

print( 20 / 6) # 3.3333333333333335

print(20 % 6) # 2

# Aritmetich Operators with String
print('String 1' + 'String 2') # String 1String 2

print('-string 1- ' * 4) # -string 1- -string 1- -string 1- -string 1-

# Comparison Operators

print(True == True) # True

#LOgical Operators  and, or
print(True and True) # True

print( True and False) # False

print(True or False) # True
print(False or False) # True

print(not True) # False
print(not False) # True

# Membership operators
print(1 in [1,2,3,4,5]) # True
print(10 not in [1,2,3,4,5]) # True

print('cat' in 'my pet cat')  # True
print('cat' not in 'my pet cat')  # False





